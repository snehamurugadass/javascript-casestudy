// Array 
let s = [3, 2, 3, 4, 5];
function Gfg() {

    // Storing the first item in a variable 
    let f = s[0];

    // Storing the last item 
    let l = s[s.length - 1];

    // Printing output to screen
    console.log("First element is " + f);
    console.log("Last element is " + l);
}
Gfg(); // Calling the function